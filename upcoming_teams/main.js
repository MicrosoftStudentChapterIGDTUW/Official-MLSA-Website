const showSocial = (card1toggle, card1social) => {
    const toggle = document.getElementById(card1toggle),
      social = document.getElementById(card1social);
  
    toggle.addEventListener("click", () => {
      //If animation class exist = down-animaiton class add
      if (social.classList.contains("animation")) {
        social.classList.add("down-animation");
  
        setTimeout(() => {
          social.classList.remove("down-animation");
        }, 1000);
      }
      //Add the animation to the div tag card__social
      social.classList.toggle("animation");
    });
  };
  
  showSocial("card1toggle", "card1social");

